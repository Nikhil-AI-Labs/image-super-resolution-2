def augment_img(img, mode=0):
    if mode == 0:
        return img
    elif mode == 1:
        return np.flipud(np.rot90(img))
    elif mode == 2:
        return np.flipud(img)
    elif mode == 3:
        return np.rot90(img, k=3)
    elif mode == 4:
        return np.flipud(np.rot90(img, k=2))
    elif mode == 5:
        return np.rot90(img)
    elif mode == 6:
        return np.rot90(img, k=2)
    elif mode == 7:
        return np.flipud(np.rot90(img, k=3))


def augment_img_tensor(img, mode=0):
    img_size = img.size()
    img_np = img.data.cpu().numpy()
    if len(img_size) == 3:
        img_np = np.transpose(img_np, (1, 2, 0))
    elif len(img_size) == 4:
        img_np = np.transpose(img_np, (2, 3, 1, 0))
    img_np = augment_img(img_np, mode=mode)
    img_tensor = torch.from_numpy(np.ascontiguousarray(img_np))
    if len(img_size) == 3:
        img_tensor = img_tensor.permute(2, 0, 1)
    elif len(img_size) == 4:
        img_tensor = img_tensor.permute(3, 2, 0, 1)

    return img_tensor.type_as(img)


def augment_imgs(img_list, hflip=True, rot=True):
    # horizontal flip OR rotate
    hflip = hflip and random.random() < 0.5
    vflip = rot and random.random() < 0.5
    rot90 = rot and random.random() < 0.5

    def _augment(img):
        if hflip:
            img = img[:, ::-1, :]
        if vflip:
            img = img[::-1, :, :]
        if rot90:
            img = img.transpose(1, 0, 2)
        return img

    return [_augment(img) for img in img_list]


def cal_psnr_ssim(output_image_path, target_image_path, border=4):
    """Calculate PSNR and SSIM on Y channel."""
    output_img = imread_uint(output_image_path, n_channels=3)
    target_img = imread_uint(target_image_path, n_channels=3)

    # Crop border
    if border > 0:
        output_img = output_img[border:-border, border:-border, :]
        target_img = target_img[border:-border, border:-border, :]

    # Convert to Y channel
    output_y = cv2.cvtColor(output_img, cv2.COLOR_RGB2YCrCb)[:, :, 0].astype(np.float64)
    target_y = cv2.cvtColor(target_img, cv2.COLOR_RGB2YCrCb)[:, :, 0].astype(np.float64)

    # PSNR
    mse = np.mean((output_y - target_y) ** 2)
    if mse == 0:
        psnr = float('inf')
    else:
        psnr = 10 * np.log10(255.0 ** 2 / mse)

    # SSIM
    from skimage.metrics import structural_similarity
    ssim = structural_similarity(output_y, target_y, data_range=255.0)

    return psnr, ssim
